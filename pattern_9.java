class pattern_9
{
 public static void main(String []args)
{
 
 for(int k=1; k<=5; k++)
 {
  for(int l=5; l>=1; l--)
  {
    if(k<l)
   System.out.print(" ");
    else
   System.out.print("*");
  
  }
 System.out.println();
}
for(int i=2; i<=5; i++)
 {
  for(int j=1; j<=5; j++)
  {
    if(j<i)
   System.out.print(" ");
    else
   System.out.print("*");
  
  }
 System.out.println();
 
}
}
}
